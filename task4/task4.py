info["ЕГЭ"].pop("Иностранный язык")

# Создать ключ "вузы" и поместить в него пустой словарь
info["вузы"] = {}

# В словарь info["вузы"] добавить информацию о вузах, где ключ - аббревиатура вуза, а значение -
# количество баллов для специальности, на которую хотели поступить
