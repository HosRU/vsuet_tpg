# Напечатать словарь
print(info)

# Создать ключ "хобби" со значением = список из строк -
# наименований хобби (например "плавание" и т.д.)
info["хобби"] = ["плавание", "пение", "гейминг"]


# Добавить "программирование" в список хобби
